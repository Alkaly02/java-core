package models;

public class BibliothequeElement {
	private String titre, auteur;
	private int anneePublication;
	
	public BibliothequeElement(String titre, String auteur, int anneePublication) {
		this.titre = titre;
		this.auteur = auteur;
		this.anneePublication = anneePublication;
	}
	
	public void setTitre(String titre) {
		this.titre = titre;
	}
	public String getTitre() {
		return this.titre;
	}
	
	public void setAuteur(String auteur) {
		this.auteur = auteur;
	}
	public String getAuteur() {
		return this.auteur;
	}
	
	public void setAnneePublication(int anneePublication) {
		this.anneePublication = anneePublication;
	}
	public int getAnneePublication() {
		return this.anneePublication;
	}
	
	public void afficherDetails() {
		System.out.println("Titre: " + this.getTitre() + "\n" + 
							"Auteur: " + this.getAuteur() + "\n" + 
							"Annee publication: " + this.getAnneePublication());
	}
}
	